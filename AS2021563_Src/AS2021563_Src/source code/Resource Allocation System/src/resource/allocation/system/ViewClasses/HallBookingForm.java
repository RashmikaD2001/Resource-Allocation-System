    public void Load(){
        
        HallController hc = new HallController();
        ArrayList<Availability> availabilty = hc.checkAvailability();
        
        DefaultTableModel DFT = (DefaultTableModel) hallTable.getModel();
        
        DFT.setRowCount(0);
        
        for(Availability a:availabilty){
            
            String idHall = a.getHall().getIdHall();
            String size = a.getHall().getType().toString();
            int capacity = a.getHall().getType().getCapacity();
            int charge = a.getHall().getType().getCharge();
            
            DFT.addRow(new Object[]{idHall,size,capacity,charge});
        }
    }

