    private void bookbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookbtActionPerformed
        // TODO add your handling code here:

        try {
            BookingController bc = new BookingController();
            SpecificDayController sdc = new SpecificDayController();
            HallController hc = new HallController();
            HallBookingForm hbf = new HallBookingForm();

            Date startD = start.getCalendar().getTime();
            Date endD = end.getCalendar().getTime();

            LocalDate startDate = LocalDate.ofInstant(startD.toInstant(), ZoneId.systemDefault());
            LocalDate endDate = LocalDate.ofInstant(endD.toInstant(), ZoneId.systemDefault());
            String type = bookingType.getSelectedItem().toString();
            String idHall = id.getText();

            Hall hall = new Hall(idHall, HallType.SMALL);
            Booking booking;

            if ((endDate.isAfter(startDate) || endDate.isEqual(startDate)) && hc.isValidIdHall(idHall)) {

                if (bc.isBooked(idHall, startDate, endDate)) {
                    JOptionPane.showMessageDialog(null, "Sorry hall is booked on that days");
                } else {
                    if (type == "Continues Period") {
                        booking = new ContinuesPeriod(customer, hall, startDate, endDate);

                        bc.save(booking, type);
                    } else {

                        String[] days = {"SUNDAY", "MONDAY", "TUESDAY", "WEDNESDAY", "THURSDAY", "FRIDAY", "SATURDAY"};
                        JComboBox<String> comboBox = new JComboBox<>(days);

                        Object[] options = {"OK", "Cancel", comboBox};

                        int choice = JOptionPane.showOptionDialog(null, "Select an option:", "Select specific day",
                                JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, options, options[0]);

                        if (choice == JOptionPane.OK_OPTION) {
                            String selectedOption = (String) comboBox.getSelectedItem();
                            DayOfWeek day = DayOfWeek.valueOf(selectedOption);
                            booking = new SpecificDay(customer, hall, startDate, endDate, day);
                            bc.save(booking, type);
                            sdc.save((SpecificDay) booking);
                        }

                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "This is not valid booking");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_bookbtActionPerformed

